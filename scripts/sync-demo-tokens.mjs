// Emit a shared demo-token catalog ({address, symbol, name}) consumed by the backend (token
// search/resolve) and the app fixtures. Source: blockchain/config/testnet.json params.demo
// (stocks + scene), with display names joined from tools/registry/out/registry.json by ticker.
// Run: node scripts/sync-demo-tokens.mjs  (re-run when the demo universe changes).
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const TESTNET = join(ROOT, "blockchain", "config", "testnet.json");
const REGISTRY = join(ROOT, "tools", "registry", "out", "registry.json");
const OUT = join(ROOT, "packages", "contracts", "src", "demo-tokens.ts");

const testnet = JSON.parse(readFileSync(TESTNET, "utf8"));
const demo = testnet.params?.demo ?? {};
const stocks = demo.stocks ?? {};
const scene = demo.scene ?? { stocks: [], names: [] };

// ticker -> display name from the registry
const nameByTicker = {};
try {
  for (const t of JSON.parse(readFileSync(REGISTRY, "utf8")).tokens ?? []) {
    if (t.ticker && t.name) nameByTicker[t.ticker.toUpperCase()] = t.name;
  }
} catch {
  /* registry optional; fall back to ticker as name */
}

// ticker -> demo price (synthetic, USD) from testnet.json
const priceByTicker = {};
for (const [ticker, v] of Object.entries(stocks)) {
  if (typeof v.priceUsd === "number") priceByTicker[ticker.toUpperCase()] = v.priceUsd;
}

const seen = new Set();
const out = [];
const push = (address, symbol, name, priceUsd) => {
  const a = String(address).toLowerCase();
  if (!/^0x[0-9a-f]{40}$/.test(a) || seen.has(a)) return;
  seen.add(a);
  out.push({ address: a, symbol, name, priceUsd });
};

// 300 demo stocks (ticker-keyed)
for (const [ticker, v] of Object.entries(stocks)) {
  push(v.address, ticker, nameByTicker[ticker.toUpperCase()] ?? ticker, Number(v.priceUsd ?? 100));
}
// scene tokens (MSTRx/TSLAx/NVDAx) — base ticker = symbol minus a trailing x
(scene.stocks ?? []).forEach((addr, i) => {
  const symbol = scene.names?.[i] ?? `S${i}`;
  const base = symbol.replace(/x$/i, "").toUpperCase();
  push(addr, symbol, nameByTicker[base] ?? symbol, priceByTicker[base] ?? 100);
});

const body =
  "// AUTO-GENERATED by scripts/sync-demo-tokens.mjs — do not edit by hand.\n" +
  "// Shared demo-token catalog (address/symbol/name) for token search + resolve.\n\n" +
  "export interface DemoToken {\n  address: string;\n  symbol: string;\n  name: string;\n  priceUsd: number;\n}\n\n" +
  `export const demoTokens: DemoToken[] = ${JSON.stringify(out, null, 2)};\n`;

writeFileSync(OUT, body);
console.log(`sync-demo-tokens: wrote ${out.length} tokens -> ${OUT}`);
